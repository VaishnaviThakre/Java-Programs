
public class StarProgram2 {

   
    public static void main(String[] args) {
        
        int row=5,i,j;
        for(i=row;i>=1;i--)
        {
            
            for(j=0;j<=i;j++)
            {
                System.out.print("*");
            }
            System.out.println();
           
    
        }
        
    }
    
}
